--  이후 좋아요 취소로 4로 내려갔다가 다시 5로 올라가도 이미 5는 지급했으므로 재지급하지 않음)
ALTER TABLE review ADD COLUMN review_rewarded_like_count INT NOT NULL DEFAULT 0 AFTER review_hit;
